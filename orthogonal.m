A=[3 2 1 1];
n4=conv(LegendrePoly(4),LegendrePoly(4));
t4=length(n4):-1:1;
k4=0;
n4=[n4./t4,k4];
n4=polyval(n4,1)-polyval(n4,-1);
n3=conv(LegendrePoly(3),LegendrePoly(3));
t3=length(n3):-1:1;
k3=0;
n3=[n3./t3,k3];
n3=polyval(n3,1)-polyval(n3,-1);
n2=conv(LegendrePoly(2),LegendrePoly(2));
t2=length(n2):-1:1;
k2=0;
n2=[n2./t2,k2];
n2=polyval(n2,1)-polyval(n2,-1);
n1=conv(LegendrePoly(1),LegendrePoly(1));
t1=length(n1):-1:1;
k1=0;
n1=[n1./t1,k1];
n1=polyval(n1,1)-polyval(n1,-1);
n0=conv(LegendrePoly(0),LegendrePoly(0));
t0=length(n0):-1:1;
k0=0;
n0=[n0./t0,k0];
n0=polyval(n0,1)-polyval(n0,-1);


d4=conv(A,LegendrePoly(4));
tt4=length(d4):-1:1;
kk4=0;
d4=[d4./tt4,kk4];
d4=polyval(d4,1)-polyval(d4,-1);
d3=conv(A,LegendrePoly(3));
tt3=length(d3):-1:1;
kk3=0;
d3=[d3./tt3,kk3];
d3=polyval(d3,1)-polyval(d3,-1);
d2=conv(A,LegendrePoly(2));
tt2=length(d2):-1:1;
kk2=0;
d2=[d2./tt2,kk2];
d2=polyval(d2,1)-polyval(d2,-1);
d1=conv(A,LegendrePoly(1));
tt1=length(d1):-1:1;
kk1=0;
d1=[d1./tt1,kk1];
d1=polyval(d1,1)-polyval(d1,-1);
d0=conv(A,LegendrePoly(0));
tt0=length(d0):-1:1;
kk0=0;
d0=[d0./tt0,kk0];
d0=polyval(d0,1)-polyval(d0,-1);

c4=d4/n4;c3=d3/n3;c2=d2/n2;c1=d1/n1;c0=d0/n0;
display([c4 c3 c2 c1 c0])

xx=-1:0.005:1;
p3=LegendrePoly(3);
p2=[zeros(1,length(p3)-3+0),LegendrePoly(2)];
p1=[zeros(1,length(p3)-3+1),LegendrePoly(1)];
p0=[zeros(1,length(p3)-3+2),LegendrePoly(0)];
p=c3*p3+c2*p2+c1*p1+c0*p0;
yy=polyval(p,xx);
y=polyval(A,xx);
plot(xx,yy,'o',xx,y,'-');
